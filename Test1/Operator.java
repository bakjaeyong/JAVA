
public class Operator {
	final static int SECOND = 1000;
	public static void main(String[] args) {
		//증감 연산자 ++ --
		int a = 10;
		System.out.println("현재의 a는" + a + "입니다.");
		a++;
		System.out.println("현재의 a는" + a + "입니다.");
		System.out.println("현재의 a는" + ++a + "입니다.");
		System.out.println("현재의 a는" + --a + "입니다.");
	}

}
